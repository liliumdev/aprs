CREATE TABLE IF NOT EXISTS `aprs`.`packet` (
  `id` INT NOT NULL AUTO_INCREMENT,
  `latitude` DECIMAL(10,8) NULL,
  `longitude` DECIMAL(11,8) NULL,
  `altitude` FLOAT NULL,
  `from` VARCHAR(45) NULL,
  `to` VARCHAR(45) NULL,
  `format` VARCHAR(45) NULL,
  `symbol` VARCHAR(5) NULL,
  `symbol_table` VARCHAR(5) NULL,
  `comment` VARCHAR(500) NULL,
  `created_at` TIMESTAMP NOT NULL,
  `updated_at` TIMESTAMP NULL,
  `object_name` VARCHAR(255) NULL,
  `speed` FLOAT NULL,
  `message_text` VARCHAR(500) NULL,
  `raw` VARCHAR(1023) NULL,
  `zone_z4` INT NULL,
  `zone_z7` INT NULL,
  `hash` VARCHAR(256) NULL,
  PRIMARY KEY (`id`),
  INDEX `lat_lon_index` (`latitude` ASC, `longitude` ASC),
  INDEX `zone_4_index` (`zone_z4` ASC),
  INDEX `zone_7_index` (`zone_z7` ASC),
  INDEX `hash_index` (`hash` ASC),
  INDEX `from_index` (`from` ASC));

CREATE TABLE IF NOT EXISTS `aprs`.`zone` (
  `id` INT NOT NULL AUTO_INCREMENT,
  `zoom` INT NULL,
  `lat_from` DECIMAL(10,8) NULL,
  `lat_to` DECIMAL(10,8) NULL,
  `long_from` DECIMAL(11,8) NULL,
  `long_to` DECIMAL(11,8) NULL,
  `center_lat` DECIMAL(10,8) NULL,
  `center_long` DECIMAL(11,8) NULL,
  PRIMARY KEY (`id`),
  INDEX `lat_lon_index` (`lat_from` ASC, `lat_to` ASC, `long_from` ASC, `long_to` ASC));

CREATE TABLE IF NOT EXISTS `aprs`.`tracker` (
  `id` INT NOT NULL AUTO_INCREMENT,
  `name` VARCHAR(255) NOT NULL,
  `lat_from` DECIMAL(10,8) NULL,
  `lat_to` DECIMAL(10,8) NULL,
  `long_from` DECIMAL(11,8) NULL,
  `long_to` DECIMAL(11,8) NULL,
  `created_at` TIMESTAMP NULL,
  `updated_at` TIMESTAMP NULL,
  PRIMARY KEY (`id`),
  INDEX `name_index` (`name` ASC));